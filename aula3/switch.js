// utilizando switch/case

let fruta = "limao";
switch (fruta){
    case "banana":
        console.log("A fruta é docinha!!");
        break;
    case "laranja":
        console.log("A fruta é ácida!!");
        break;
    case "limao":
        console.log("A fruta também é ácida!!");
        break;
    default:
        console.log("Qual é fruta?");
}